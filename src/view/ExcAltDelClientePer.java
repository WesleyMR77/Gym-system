    private void btnConfirmarAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarAltActionPerformed
        ClientePer c = new ClientePer();

        c.setId(Integer.parseInt(txtId.getText()));
        c.setNome(txtNome.getText());
        c.setPeso(Double.parseDouble(txtPeso.getText()));
        c.setAltura(Double.parseDouble(txtAltura.getText()));
        c.setGenero(txtGenero.getText());
        c.setIdade(Integer.parseInt(txtIdade.getText()));
        c.setTelefone(txtTelefone.getText());
        //int pos = cbxPersonal.getSelectedIndex()-1;

        for (int i = 0; i < personais.size(); i++) {
            if (personais.get(i).getNome().equals(cbxPersonal.getSelectedItem())) {
                c.setPersonal(personais.get(i));
                JOptionPane.showMessageDialog(null, personais.get(i).getNome());
            }
        }

        dao.alterar(c);
        JOptionPane.showMessageDialog(this, "CLIENTE ALTERADO COM SUCESSO");
        ativarCampo(false);
        btnConfirmarAlt.setEnabled(false);
    }//GEN-LAST:event_btnConfirmarAltActionPerformed

