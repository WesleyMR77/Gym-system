    private void txtDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDeletarActionPerformed

        int resposta = JOptionPane.showConfirmDialog(this, "Confirma a exclusão", "Exclusão", JOptionPane.YES_NO_OPTION);
        if (resposta == 0) {
            dao.excluir(clientes.get(cont));
            JOptionPane.showMessageDialog(this, "CLIENTE EXCLUIDO COM SUCESSO");
        }
        if (cont == clientes.size() - 1) {
            cont--;
        }
        DaoObjectImp dao = new DaoObjectImp();
        List<ClientePer> clientes = new ArrayList<ClientePer>();
        clientes = (List<ClientePer>) (Object) dao.getBD("ClientePer");

        if (clientes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NÃO EXISTE CLIENTE PARA LISTAR");
            MenuClientePer mc = new MenuClientePer();
            this.dispose();
            mc.setVisible(true);
        } else {
            mostrarNaTela();
        }
    }//GEN-LAST:event_txtDeletarActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        if (cont < clientes.size() - 1) {
            cont += 1;
            mostrarNaTela();
        }
    }//GEN-LAST:event_btnProximoActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        if (cont > 0) {
            cont -= 1;
            mostrarNaTela();
        }
    }//GEN-LAST:event_btnAnteriorActionPerformed

