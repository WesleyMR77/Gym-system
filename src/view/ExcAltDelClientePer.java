    public void mostrarNaTela() {
        personais = (List<Personal>) (Object) dao.getBD("Personal");
        clientes = (List<ClientePer>) (Object) dao.getBD("ClientePer");

        txtId.setText(String.valueOf(clientes.get(cont).getId()));
        txtNome.setText(String.valueOf(clientes.get(cont).getNome()));
        txtPeso.setText(String.valueOf(clientes.get(cont).getPeso()));
        txtAltura.setText(String.valueOf(clientes.get(cont).getAltura()));
        txtGenero.setText(String.valueOf(clientes.get(cont).getGenero()));
        txtIdade.setText(String.valueOf(clientes.get(cont).getIdade()));
        txtTelefone.setText(String.valueOf(clientes.get(cont).getTelefone()));
        txtMensalidade.setText(String.valueOf(clientes.get(cont).getMensalidade()));
        cbxPersonal.removeAllItems();
        cbxPersonal.addItem(String.valueOf(clientes.get(cont).getPersonal().getNome()));

        for (int i = 0; i < personais.size(); i++) {
            if (!personais.get(i).getNome().equals(clientes.get(cont).getPersonal().getNome())) {
                cbxPersonal.addItem(String.valueOf(personais.get(i).getNome()));
            }
        }
    }

